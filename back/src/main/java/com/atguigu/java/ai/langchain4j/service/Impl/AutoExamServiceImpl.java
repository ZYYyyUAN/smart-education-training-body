    private String buildKnowledgePointsJson(List<KnowledgePoint> knowledgePoints) {
        // 构建知识点JSON字符串
        return knowledgePoints.stream()
                .map(kp -> String.format("{\"id\":%d,\"title\":\"%s\"}", 
                    kp.getId(), kp.getTitle()))
                .collect(Collectors.joining(","));
    }
    
    private ExamGenerationResult parseAIResponse(String aiResponse) {
        // 解析AI响应
        try {
            // 处理AI响应可能包含的markdown代码块
            String jsonResponse = aiResponse.trim();
            
            // 如果响应被包装在markdown代码块中，提取JSON部分
            if (jsonResponse.startsWith("```json")) {
                int startIndex = jsonResponse.indexOf("```json") + 7;
                int endIndex = jsonResponse.lastIndexOf("```");
                if (endIndex > startIndex) {
                    jsonResponse = jsonResponse.substring(startIndex, endIndex).trim();
                }
            } else if (jsonResponse.startsWith("```")) {
                int startIndex = jsonResponse.indexOf("```") + 3;
                int endIndex = jsonResponse.lastIndexOf("```");
                if (endIndex > startIndex) {
                    jsonResponse = jsonResponse.substring(startIndex, endIndex).trim();
                }
            }
            
            log.info("解析的JSON响应: {}", jsonResponse);
            
            ExamGenerationResult result = objectMapper.readValue(jsonResponse, ExamGenerationResult.class);
            
            // 处理suggestions字段，统一转换为List<String>格式
            if (result.getSuggestions() != null) {
                List<String> suggestionsList = new ArrayList<>();
                if (result.getSuggestions() instanceof String) {
                    // 如果是字符串，直接添加
                    suggestionsList.add((String) result.getSuggestions());
                } else if (result.getSuggestions() instanceof List) {
                    // 如果是列表，转换每个元素为字符串
                    @SuppressWarnings("unchecked")
                    List<Object> suggestionsObj = (List<Object>) result.getSuggestions();
                    for (Object suggestion : suggestionsObj) {
                        suggestionsList.add(suggestion.toString());
                    }
                }
                // 将处理后的列表重新设置
                result.setSuggestions(suggestionsList);
            }
            
            return result;
        } catch (Exception e) {
            log.error("解析AI响应失败，原始响应: {}", aiResponse, e);
            throw new RuntimeException("解析AI响应失败", e);
        }
    }
    
