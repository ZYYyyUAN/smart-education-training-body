    /**
     * 根据知识点和题库分类从题库中筛选题目
     */
    private List<QuestionBank> getQuestionsByKnowledgePoints(Long courseId, List<String> selectedKnowledgePoints, Long categoryId) {
        // 先检查数据库中是否有题目
        List<QuestionBank> allQuestions = questionBankMapper.selectList(new QueryWrapper<>());
        log.info("数据库中总题目数量: {}", allQuestions.size());
        
        if (allQuestions.isEmpty()) {
            log.warn("数据库中没有任何题目！");
            return allQuestions;
        }
        
        // 打印前几条题目信息用于调试
        log.info("数据库中的前3条题目:");
        allQuestions.stream().limit(3).forEach(q -> {
            log.info("题目ID: {}, 课程ID: {}, 分类ID: {}, 类型: {}, 知识点: {}, 内容: {}", 
                    q.getId(), q.getCourseId(), q.getCategoryId(), q.getType(), q.getKnowledgePoint(), 
                    q.getContent() != null && q.getContent().length() > 50 ? q.getContent().substring(0, 50) + "..." : q.getContent());
        });
        
        QueryWrapper<QuestionBank> queryWrapper = new QueryWrapper<>();
        queryWrapper.eq("course_id", courseId);
        
        // 如果指定了题库分类，添加分类筛选条件
        if (categoryId != null) {
            queryWrapper.eq("category_id", categoryId);
        }
        
        // 如果选择了知识点，添加知识点筛选条件
        if (selectedKnowledgePoints != null && !selectedKnowledgePoints.isEmpty()) {
        // 使用OR条件匹配任意一个选中的知识点
        queryWrapper.and(wrapper -> {
            for (String knowledgePoint : selectedKnowledgePoints) {
                wrapper.like("knowledge_point", knowledgePoint).or();
            }
        });
        }
        
        List<QuestionBank> questions = questionBankMapper.selectList(queryWrapper);
        log.info("筛选到的题目数量: {}, 课程ID: {}, 分类ID: {}, 知识点: {}", 
                questions.size(), courseId, categoryId, selectedKnowledgePoints);
        
        // 打印筛选后的题目信息用于调试
        if (!questions.isEmpty()) {
            log.info("筛选后的前3条题目信息:");
            questions.stream().limit(3).forEach(q -> {
                log.info("题目ID: {}, 类型: {}, 知识点: {}, 内容: {}", 
                        q.getId(), q.getType(), q.getKnowledgePoint(), 
                        q.getContent().length() > 50 ? q.getContent().substring(0, 50) + "..." : q.getContent());
            });
        } else {
            log.warn("没有找到符合条件的题目！");
            // 尝试不按知识点筛选
            QueryWrapper<QuestionBank> fallbackWrapper = new QueryWrapper<>();
            fallbackWrapper.eq("course_id", courseId);
            if (categoryId != null) {
                fallbackWrapper.eq("category_id", categoryId);
            }
            List<QuestionBank> fallbackQuestions = questionBankMapper.selectList(fallbackWrapper);
            log.info("不按知识点筛选的题目数量: {}", fallbackQuestions.size());
        }
        
        return questions;
    }
    
