    @Override
    @Transactional
    public Result<AutoGeneratedExam> generateExam(ExamGenerationRequest request) {
        try {
            long startTime = System.currentTimeMillis();
            
            // 1. 获取课程信息
            Course course = courseService.getCourseById(request.getCourseId());
            if (course == null) {
                return Result.error("课程不存在");
            }
            
            // 2. 获取知识点信息
            List<KnowledgePoint> knowledgePoints = knowledgePointMapper.selectByCourseId(request.getCourseId());
            
            // 3. 获取题库统计信息
            QuestionBankStats stats = getQuestionBankStats(request.getCourseId());
            
            // 4. 根据选中的知识点和题库分类从题库中筛选题目
            List<QuestionBank> availableQuestions = getQuestionsByKnowledgePoints(
                request.getCourseId(), 
                request.getSelectedKnowledgePoints(),
                request.getCategoryId()
            );
            
            if (availableQuestions.isEmpty()) {
                return Result.error("所选知识点下没有可用题目，请选择其他知识点或添加题目到题库");
            }
            
            // 5. 先按难度占比从题库中抽题（硬性兜底），不足则用其他难度补足
            List<Long> preSelected = enforceDifficultySampling(
                availableQuestions,
                request.getTotalQuestions(),
                request.getDifficultyDistribution()
            );

            // 6. 始终使用筛选出的题目进行组卷
            String aiResponse = autoExamAgent.generateExamFromQuestionBank(
                    request.getCourseId(),
                    course.getCourseName(),
                request.getSelectedKnowledgePoints() != null && !request.getSelectedKnowledgePoints().isEmpty() 
                    ? String.join(", ", request.getSelectedKnowledgePoints())
                    : "所有知识点",
                    request.getExamTitle(),
                    request.getExamDescription(),
                    request.getTotalQuestions(),
                    objectMapper.writeValueAsString(request.getQuestionDistribution()),
                    objectMapper.writeValueAsString(request.getDifficultyDistribution()),
                    request.getTimeLimitMinutes(),
                    request.getGenerationStrategy(),
                    buildQuestionBankJson(availableQuestions)
                );
            
            // 7. 解析AI响应
            ExamGenerationResult result = parseAIResponse(aiResponse);
            
            // 如果AI未返回题目或难度覆盖不符合预期，则用预选题构建一个保底的选择
            if (result.getQuestionSelection() == null || result.getQuestionSelection().isEmpty()) {
                List<QuestionSelection> fallback = preSelected.stream().map(qid -> {
                    QuestionBank qb = questionBankMapper.selectById(qid);
                    QuestionSelection s = new QuestionSelection();
                    s.setQuestionId(qid);
                    s.setDifficulty(mapDifficultyToInt(qb != null ? qb.getDifficultyLevel() : null));
                    s.setEstimatedTime( qb != null && qb.getEstimatedTimeMinutes()!=null ? qb.getEstimatedTimeMinutes() : 5);
                    s.setKnowledgePoint(qb != null ? qb.getKnowledgePoint() : null);
                    s.setReason("按难度占比预选");
                    return s;
                }).collect(Collectors.toList());
                result.setQuestionSelection(fallback);
            }
            // 若AI返回但难度严重偏离目标，也强制重平衡
            else {
                List<QuestionSelection> rebalanced = validateAndRebalanceSelection(availableQuestions, result.getQuestionSelection(),
                        request.getTotalQuestions(), normalizeDifficultyDistribution(request.getDifficultyDistribution()));
                if (rebalanced != null) {
                    result.setQuestionSelection(rebalanced);
                }
            }

            // 8. 创建试卷记录（仅预览，不保存到数据库）
            AutoGeneratedExam exam = createExamRecord(request, result);
            
            // 9. 将题目选择结果附加到试卷对象中，供前端预览
            exam.setQuestionSelection(result.getQuestionSelection());
            
            // 10. 记录生成日志（仅记录，不保存）
            logGeneration(request, aiResponse, result, System.currentTimeMillis() - startTime);
            
            return Result.ok(exam);
        } catch (Exception e) {
            log.error("智能组卷失败", e);
            return Result.error("智能组卷失败: " + e.getMessage());
        }
    }
    
    @Override
    public Result<ExamPreviewVO> previewExam(Long examId) {
        try {
            log.info("开始预览试卷: examId={}", examId);
            
            AutoGeneratedExam exam = autoGeneratedExamMapper.selectById(examId);
            if (exam == null) {
                log.warn("试卷不存在: examId={}", examId);
                return Result.error("试卷不存在");
            }
            
            log.info("找到试卷: examId={}, title={}", examId, exam.getExamTitle());
            
            // 获取试卷题目
            List<AutoExamQuestion> examQuestions = autoExamQuestionMapper.selectByExamId(examId);
            log.info("查询到试卷题目数量: examId={}, count={}", examId, examQuestions.size());
            
            if (examQuestions.isEmpty()) {
                log.warn("试卷题目为空: examId={}", examId);
            } else {
                for (AutoExamQuestion eq : examQuestions) {
                    log.info("试卷题目: examId={}, questionBankId={}, order={}", 
                        eq.getExamId(), eq.getQuestionBankId(), eq.getQuestionOrder());
                }
            }
            
            // 构建预览VO
            ExamPreviewVO preview = buildExamPreviewVO(exam, examQuestions);
            
            log.info("构建预览VO完成: examId={}, totalQuestions={}", examId, preview.getTotalQuestions());
            
            return Result.ok(preview);
        } catch (Exception e) {
            log.error("预览试卷失败", e);
            return Result.error("预览试卷失败: " + e.getMessage());
        }
    }
    
    @Override
    @Transactional
    public Result<AutoGeneratedExam> adjustExam(Long examId, String adjustmentType, String requirements) {
        try {
            AutoGeneratedExam exam = autoGeneratedExamMapper.selectById(examId);
            if (exam == null) {
                return Result.error("试卷不存在");
            }
            
            // 直接处理简单调整（remove/replace/add），避免AI响应markdown导致解析失败
            if ("remove".equalsIgnoreCase(adjustmentType)) {
                Long qid = extractQuestionId(requirements);
                if (qid == null) {
                    return Result.error("缺少questionId，无法删除");
                }
                autoExamQuestionMapper.deleteByExamAndQuestion(examId, qid);
                reorderExamQuestions(examId);
                return Result.ok(exam);
            }
            
            // 获取当前题目
            List<AutoExamQuestion> currentQuestions = autoExamQuestionMapper.selectByExamId(examId);
            
            // 获取可选题目（此处示例用courseId，生产应根据需求过滤）
            List<QuestionBank> availableQuestions = questionBankMapper.selectList(new QueryWrapper<>());
            
            // 调用AI调整
            String aiResponse = autoExamAgent.adjustExam(
                examId,
                buildCurrentQuestionsJson(currentQuestions),
                adjustmentType,
                requirements,
                buildAvailableQuestionsJson(availableQuestions)
            );
            
            // 解析调整建议并应用（parseAdjustmentResponse内部已处理markdown代码块）
            ExamAdjustmentResult adjustmentResult = parseAdjustmentResponse(aiResponse);
            applyAdjustment(examId, adjustmentResult);
            
            return Result.ok(exam);
        } catch (Exception e) {
            log.error("调整试卷失败", e);
            return Result.error("调整试卷失败: " + e.getMessage());
        }
    }
    
