    /** 将题库难度字符串映射为整型：easy=1, medium=2, hard=3（默认2） */
    private int mapDifficultyToInt(String difficultyLevel) {
        String norm = getNormalizedDifficultyLevel(difficultyLevel);
        switch (norm) {
            case "easy": return 1;
            case "hard": return 3;
            default: return 2;
        }
    }

    /** 规范化难度字符串，兼容中文与别名 */
    private String getNormalizedDifficultyLevel(String level) {
        if (level == null) return "medium";
        String val = level.trim().toLowerCase();
        if ("简单".equals(level) || "易".equals(level) || "容易".equals(level) || "1".equals(val)) return "easy";
        if ("中等".equals(level) || "中".equals(level) || "2".equals(val)) return "medium";
        if ("困难".equals(level) || "难".equals(level) || "3".equals(val)) return "hard";
        if (val.contains("easy")) return "easy";
        if (val.contains("hard")) return "hard";
        if (val.contains("medium")) return "medium";
        return "medium";
    }

    /**
     * 根据难度分布从可用题目中抽样，返回题目ID列表。
     */
    private List<Long> enforceDifficultySampling(List<QuestionBank> pool, int total,
                                                 Map<String, Double> difficultyDistribution) {
        if (pool == null || pool.isEmpty()) return Collections.emptyList();
        Map<String, List<QuestionBank>> byDiff = pool.stream()
                .collect(Collectors.groupingBy(q -> getNormalizedDifficultyLevel(q.getDifficultyLevel())));

        Map<String, Double> dist = normalizeDifficultyDistribution(difficultyDistribution);
        int easyTarget = (int) Math.round(total * dist.getOrDefault("easy", 0.0));
        int medTarget  = (int) Math.round(total * dist.getOrDefault("medium", 0.0));
        int hardTarget = (int) Math.round(total * dist.getOrDefault("hard", 0.0));

        log.info("难度分布抽样目标: total={}, easy={}, medium={}, hard={} | pool: easy={}, medium={}, hard={}",
                total, easyTarget, medTarget, hardTarget,
                byDiff.getOrDefault("easy", Collections.emptyList()).size(),
                byDiff.getOrDefault("medium", Collections.emptyList()).size(),
                byDiff.getOrDefault("hard", Collections.emptyList()).size());

        List<Long> result = new ArrayList<>();
        pickRandom(byDiff.get("easy"), easyTarget, result);
        pickRandom(byDiff.get("medium"), medTarget, result);
        pickRandom(byDiff.get("hard"), hardTarget, result);

        // 若不足总数，用其它难度补足
        if (result.size() < total) {
            List<QuestionBank> remaining = new ArrayList<>(pool);
            // 去重
            remaining.removeIf(q -> result.contains(q.getId()));
            Collections.shuffle(remaining);
            for (QuestionBank q : remaining) {
                if (result.size() >= total) break;
                result.add(q.getId());
            }
        }
        return result;
    }

    private void pickRandom(List<QuestionBank> list, int target, List<Long> out) {
        if (target <= 0 || list == null || list.isEmpty()) return;
        List<QuestionBank> copy = new ArrayList<>(list);
        Collections.shuffle(copy);
        // 按目标数量截取
        int need = Math.min(target, copy.size());
        int added = 0;
        for (QuestionBank qb : copy) {
            if (added >= need) break;
            if (!out.contains(qb.getId())) { out.add(qb.getId()); added++; }
        }
    }

    /**
     * 校正AI返回的题目集合的难度分布；若偏差过大，则按目标分布重采样。
     */
    private List<QuestionSelection> validateAndRebalanceSelection(List<QuestionBank> pool,
                                                                  List<QuestionSelection> aiSel,
                                                                  int total,
                                                                  Map<String, Double> targetDist) {
        if (aiSel == null || aiSel.isEmpty()) return null;
        Map<String, Long> count = new HashMap<>();
        for (QuestionSelection s : aiSel) {
            QuestionBank qb = pool.stream().filter(q -> Objects.equals(q.getId(), s.getQuestionId())).findFirst().orElse(null);
            String d = getNormalizedDifficultyLevel(qb != null ? qb.getDifficultyLevel() : null);
            count.merge(d, 1L, Long::sum);
        }
        double easyRate = count.getOrDefault("easy", 0L) * 1.0 / aiSel.size();
        double medRate  = count.getOrDefault("medium", 0L) * 1.0 / aiSel.size();
        double hardRate = count.getOrDefault("hard", 0L) * 1.0 / aiSel.size();
        log.info("AI返回难度比例: easy={}, medium={}, hard={} | 目标: {}", easyRate, medRate, hardRate, targetDist);

        // 偏差阈值：任一难度偏离目标超过30%则认为需要重平衡
        boolean needRebalance = Math.abs(easyRate - targetDist.getOrDefault("easy", 0.0)) > 0.3
                || Math.abs(medRate - targetDist.getOrDefault("medium", 0.0)) > 0.3
                || Math.abs(hardRate - targetDist.getOrDefault("hard", 0.0)) > 0.3;
        if (!needRebalance) return null;

        List<Long> ids = enforceDifficultySampling(pool, total, targetDist);
        List<QuestionSelection> fixed = ids.stream().map(id -> {
            QuestionBank qb = pool.stream().filter(q -> Objects.equals(q.getId(), id)).findFirst().orElse(null);
            QuestionSelection s = new QuestionSelection();
            s.setQuestionId(id);
            s.setDifficulty(mapDifficultyToInt(qb != null ? qb.getDifficultyLevel() : null));
            s.setEstimatedTime(qb != null && qb.getEstimatedTimeMinutes()!=null ? qb.getEstimatedTimeMinutes() : 5);
            s.setKnowledgePoint(qb != null ? qb.getKnowledgePoint() : null);
            s.setReason("重平衡难度占比");
            return s;
        }).collect(Collectors.toList());
        return fixed;
    }

    /** 规范化来自前端的难度分布，兼容中文键与百分比 */
    private Map<String, Double> normalizeDifficultyDistribution(Map<String, Double> raw) {
        Map<String, Double> dist = new HashMap<>();
        if (raw != null) {
            for (Map.Entry<String, Double> e : raw.entrySet()) {
                String key = e.getKey();
                Double v = Optional.ofNullable(e.getValue()).orElse(0.0);
                String normKey;
                if ("简单".equals(key) || "易".equals(key) || "easy".equalsIgnoreCase(key)) normKey = "easy";
                else if ("中等".equals(key) || "中".equals(key) || "medium".equalsIgnoreCase(key)) normKey = "medium";
                else if ("困难".equals(key) || "难".equals(key) || "hard".equalsIgnoreCase(key)) normKey = "hard";
                else normKey = key.toLowerCase();
                dist.merge(normKey, v, Double::sum);
            }
        }
        // 如果像百分比一样 > 1，则按100缩放
        double sum = dist.values().stream().mapToDouble(d -> d).sum();
        if (sum > 1.01) {
            dist.replaceAll((k, v) -> v / 100.0);
            sum = dist.values().stream().mapToDouble(d -> d).sum();
        }
        // 归一化到和为1
        if (sum == 0) {
            dist.put("medium", 1.0);
        } else {
            final double total = sum;
            dist.replaceAll((k, v) -> v / total);
        }
        // 确保三个键都存在
        dist.putIfAbsent("easy", 0.0);
        dist.putIfAbsent("medium", 0.0);
        dist.putIfAbsent("hard", 0.0);
        return dist;
    }
    
