    private void saveExamQuestions(Long examId, List<QuestionSelection> questionSelections) {
        // 保存试卷题目关联
        List<AutoExamQuestion> examQuestions = new ArrayList<>();
        
        // 去重处理：使用Set来跟踪已添加的题目ID
        Set<Long> addedQuestionIds = new HashSet<>();
        int order = 1;
        
        for (QuestionSelection selection : questionSelections) {
            // 如果题目ID已经存在，跳过重复的题目
            if (addedQuestionIds.contains(selection.getQuestionId())) {
                log.warn("跳过重复题目: questionId={}, reason={}", 
                    selection.getQuestionId(), selection.getReason());
                continue;
            }
            
            AutoExamQuestion examQuestion = new AutoExamQuestion();
            examQuestion.setExamId(examId);
            examQuestion.setQuestionBankId(selection.getQuestionId());
            examQuestion.setQuestionOrder(order++);
            // 难度优先使用题库自身难度，其次使用AI返回的难度
            Integer difficulty = selection.getDifficulty();
            if (difficulty == null) {
                QuestionBank qb = questionBankMapper.selectById(selection.getQuestionId());
                difficulty = mapDifficultyToInt(qb != null ? qb.getDifficultyLevel() : null);
            }
            examQuestion.setDifficultyLevel(difficulty);
            examQuestion.setEstimatedTimeMinutes(selection.getEstimatedTime());
            examQuestion.setAiSelectionReason(selection.getAiSelectionReason() != null ? 
                selection.getAiSelectionReason() : selection.getReason());
            examQuestion.setCreatedAt(LocalDateTime.now());
            
            // 如果有知识点信息，尝试获取知识点ID
            if (selection.getKnowledgePoint() != null) {
                Long knowledgePointId = getKnowledgePointIdByName(selection.getKnowledgePoint());
                examQuestion.setKnowledgePointId(knowledgePointId);
            }
            
            examQuestions.add(examQuestion);
            addedQuestionIds.add(selection.getQuestionId());
        }
        
        log.info("去重后实际保存题目数量: {}/{}", examQuestions.size(), questionSelections.size());
        autoExamQuestionMapper.insertBatch(examQuestions);
    }
    
