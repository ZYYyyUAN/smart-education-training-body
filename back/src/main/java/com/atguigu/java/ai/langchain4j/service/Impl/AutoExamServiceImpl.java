    @Override
    @Transactional
    public Result<String> publishExam(Long examId, Long teacherId) {
        try {
            AutoGeneratedExam exam = autoGeneratedExamMapper.selectById(examId);
            if (exam == null) {
                return Result.error("试卷不存在");
            }
            
            exam.setStatus("published");
            exam.setPublishedAt(LocalDateTime.now());
            autoGeneratedExamMapper.updateById(exam);

            // 同步创建正式考试记录，供"考试总览"展示
            Long courseId = null;
            List<AutoExamQuestion> eqs = autoExamQuestionMapper.selectByExamId(examId);
            if (!eqs.isEmpty()) {
                QuestionBank qb = questionBankMapper.selectById(eqs.get(0).getQuestionBankId());
                if (qb != null) courseId = qb.getCourseId();
            }
            com.atguigu.java.ai.langchain4j.entity.Exam formal = new com.atguigu.java.ai.langchain4j.entity.Exam();
            formal.setCourseId(courseId);
            formal.setTeacherId(teacherId); // 使用传入的teacherId而不是exam.getCreatedBy()
            formal.setTitle(exam.getExamTitle());
            formal.setDescription(exam.getExamDescription());
            formal.setExamDate(LocalDate.now());
            formal.setDurationMinutes(120);
            formal.setCreatedAt(LocalDateTime.now());
            examMapper.insert(formal);
            
            // 同步创建exam_question关联记录，供学生端查询
            Long formalExamId = formal.getId();
            for (AutoExamQuestion aeq : eqs) {
                // 插入到exam_question表
                examQuestionMapper.insertExamQuestion(formalExamId, aeq.getQuestionBankId());
            }
            
            return Result.ok();
        } catch (Exception e) {
            log.error("发布试卷失败", e);
            return Result.error("发布试卷失败: " + e.getMessage());
        }
    }
    
    @Override
    @Transactional
    public Result<String> publishExamDirect(AutoGeneratedExam examData, Long teacherId, String deadline) {
        try {
            // 1. 先保存试卷到auto_generated_exam表
            examData.setStatus("published");
            examData.setCreatedAt(LocalDateTime.now());
            examData.setPublishedAt(LocalDateTime.now());
            examData.setCreatedBy(teacherId);
            autoGeneratedExamMapper.insert(examData);
            
            // 2. 保存题目关联到auto_exam_question表
            if (examData.getQuestionSelection() != null && !examData.getQuestionSelection().isEmpty()) {
                saveExamQuestions(examData.getId(), examData.getQuestionSelection());
            }
            
            // 3. 创建正式考试记录，供"考试总览"展示
            Long courseId = null;
            if (examData.getQuestionSelection() != null && !examData.getQuestionSelection().isEmpty()) {
                QuestionBank qb = questionBankMapper.selectById(examData.getQuestionSelection().get(0).getQuestionId());
                if (qb != null) courseId = qb.getCourseId();
            }
            
            com.atguigu.java.ai.langchain4j.entity.Exam formal = new com.atguigu.java.ai.langchain4j.entity.Exam();
            formal.setCourseId(courseId);
            formal.setTeacherId(teacherId);
            formal.setTitle(examData.getExamTitle());
            formal.setDescription(examData.getExamDescription());
            // 使用前端传递的截止日期，如果没有则使用当前日期
            if (deadline != null && !deadline.trim().isEmpty()) {
                try {
                    // 解析截止日期字符串，格式为 "YYYY-MM-DD HH:mm:ss"
                    LocalDateTime deadlineDateTime = LocalDateTime.parse(deadline, 
                        java.time.format.DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss"));
                    formal.setExamDate(deadlineDateTime.toLocalDate());
                } catch (Exception e) {
                    log.warn("解析截止日期失败，使用当前日期: {}", deadline, e);
                    formal.setExamDate(LocalDate.now());
                }
            } else {
                formal.setExamDate(LocalDate.now());
            }
            formal.setDurationMinutes(120); // 默认120分钟
            formal.setCreatedAt(LocalDateTime.now());
            examMapper.insert(formal);
            
            // 4. 同步创建exam_question关联记录，供学生端查询
            Long formalExamId = formal.getId();
            if (examData.getQuestionSelection() != null) {
                for (QuestionSelection qs : examData.getQuestionSelection()) {
                    examQuestionMapper.insertExamQuestion(formalExamId, qs.getQuestionId());
                }
            }
            
            return Result.ok("发布成功");
        } catch (Exception e) {
            log.error("直接发布试卷失败", e);
            return Result.error("发布试卷失败: " + e.getMessage());
        }
    }
    
