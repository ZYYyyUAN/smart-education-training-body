    private String buildQuestionBankJson(List<QuestionBank> questions) {
        try {
            List<Map<String, Object>> questionList = questions.stream().map(q -> {
                Map<String, Object> questionMap = new HashMap<>();
                questionMap.put("id", q.getId());
                questionMap.put("type", q.getType());
                questionMap.put("content", q.getContent());
                questionMap.put("answer", q.getAnswer());
                questionMap.put("knowledgePoint", q.getKnowledgePoint());
                questionMap.put("difficultyLevel", q.getDifficultyLevel());
                questionMap.put("categoryId", q.getCategoryId());
                questionMap.put("estimatedTimeMinutes", q.getEstimatedTimeMinutes());
                questionMap.put("tags", q.getTags());
                questionMap.put("isAiGenerated", q.getIsAiGenerated());
                questionMap.put("qualityScore", q.getQualityScore());
                questionMap.put("createdBy", q.getCreatedBy());
                // 将LocalDateTime转换为字符串
                questionMap.put("createdAt", q.getCreatedAt() != null ? q.getCreatedAt().toString() : null);
                return questionMap;
            }).collect(Collectors.toList());
            
            Map<String, Object> result = new HashMap<>();
            result.put("totalQuestions", questions.size());
            result.put("questions", questionList);
            
            // 按题型分组统计
            Map<String, Long> typeCount = questions.stream()
                .collect(Collectors.groupingBy(QuestionBank::getType, Collectors.counting()));
            result.put("typeDistribution", typeCount);
            
            // 按知识点分组统计
            Map<String, Long> knowledgeCount = questions.stream()
                .collect(Collectors.groupingBy(QuestionBank::getKnowledgePoint, Collectors.counting()));
            result.put("knowledgeDistribution", knowledgeCount);
            
            // 按难度分组统计
            Map<String, Long> difficultyCount = questions.stream()
                .collect(Collectors.groupingBy(QuestionBank::getDifficultyLevel, Collectors.counting()));
            result.put("difficultyDistribution", difficultyCount);
            
            return objectMapper.writeValueAsString(result);
        } catch (Exception e) {
            log.error("构建题库JSON失败", e);
            return "{}";
        }
    }
